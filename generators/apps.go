type FirefoxPrefsGenerator struct{}

func (FirefoxPrefsGenerator) Name() string       { return "Firefox Prefs" }
func (FirefoxPrefsGenerator) OutputPath() string { return ".config/firefox-theme/user-prefs.js" }
func (FirefoxPrefsGenerator) Generate(theme *rainbeau.Theme, wallpapersDir string) (string, error) {
	override := "0"
	if strings.Contains(strings.ToLower(theme.Gtk.ColorScheme), "light") {
		override = "1"
	}
	return fmt.Sprintf(`// Auto-generated by Rainbeau — do not edit manually
// Theme: %s
user_pref("toolkit.legacyUserProfileCustomizations.stylesheets", true);
user_pref("layout.css.prefers-color-scheme.content-override", %s);
`, theme.Name, override), nil
}

