func (OmniLauncherConfigGenerator) Generate(theme *rainbeau.Theme, wallpapersDir string) (string, error) {
	c := theme.Colors
	p := rainbeau.ResolvePalette(theme)
	fnt := theme.Font
	selectedBg := rainbeau.MixColors(p.Accent1, c.Bg0, 0.72)
	config := map[string]any{
		"fontFamily":  fnt.Family,
		"fontSize":    int(math.Max(14, float64(fnt.Size+4))),
		"giphyApiKey": "",
		"panelWidth":  720,
		"panelHeight": 520,
		"padding":     5,
		"spacing":     5,
		"radius":      int(math.Max(6, float64(theme.Hyprland.Rounding*2))),
		"colors": map[string]any{
			"background":    c.Bg0,
			"foreground":    p.Text,
			"idle":          p.TextDim,
			"accent":        p.Accent1,
			"overlayStrong": selectedBg,
			"overlayWeak":   c.Bg2,
			"border":        p.Border,
		},
	}
	content, err := json.MarshalIndent(config, "", "  ")
	return string(content) + "\n", err
}

