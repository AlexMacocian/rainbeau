type WallpaperCyclerGenerator struct{}

func (WallpaperCyclerGenerator) Name() string { return "Wallpaper Cycler" }
func (WallpaperCyclerGenerator) OutputPath() string {
	return ".config/hypr/scripts/wallpaper-cycler.sh"
}

func (WallpaperCyclerGenerator) Generate(theme *rainbeau.Theme, wallpapersDir string) (string, error) {
	imageEntries := wallpaperArrayEntries(theme.Wallpapers.Images, false)
	videoEntries := wallpaperArrayEntries(theme.Wallpapers.Videos, true)
	return fmt.Sprintf(`#!/usr/bin/env bash
# Auto-generated by Rainbeau — do not edit manually
# Theme: %s
# Cycles wallpapers (images and videos) from the theme's wallpaper lists
# Usage: wallpaper-cycler.sh [interval_seconds]
# Default interval: %d seconds

set -euo pipefail

WALLPAPER_DIR="$HOME/.config/hypr/wallpapers"
INTERVAL="${1:-%d}"
FIT_MODE="%s"

IMAGES=(
%s
)

VIDEOS=(
%s
)

# Build list of valid entries based on available tools
ENTRIES=()
if command -v hyprpaper &>/dev/null; then
    for img in "${IMAGES[@]}"; do
        ENTRIES+=("image:$img")
    done
fi
if command -v mpvpaper &>/dev/null; then
    for vid in "${VIDEOS[@]}"; do
        ENTRIES+=("video:$vid")
    done
fi

# Nothing to do if no usable entries
if [[ ${#ENTRIES[@]} -eq 0 ]]; then
    exit 0
fi

apply_wallpaper() {
    local entry="$1"
    local type="${entry%%:*}"
    local file="${entry#*:}"

    if [[ "$type" == "video" ]]; then
        killall hyprpaper 2>/dev/null || true
        killall mpvpaper 2>/dev/null || true
        sleep 0.3
        local monitors
        monitors=$(hyprctl monitors -j | jq -r '.[].name')
        for mon in $monitors; do
            mpvpaper -o "no-audio loop hwdec=auto" "$mon" "$file" &
        done
    else
        if pgrep -x mpvpaper > /dev/null; then
            killall mpvpaper 2>/dev/null || true
            sleep 0.3
        fi
        if ! pgrep -x hyprpaper > /dev/null; then
            cat > "$HOME/.config/hypr/hyprpaper.conf" <<HYPR
# Auto-generated by Rainbeau
wallpaper {
    monitor =
    path = $file
    fit_mode = $FIT_MODE
}

splash = false
ipc = true
HYPR
            hyprpaper &
            sleep 0.5
        fi
        hyprctl hyprpaper wallpaper ",$file,$FIT_MODE" 2>/dev/null || true
    fi
}

while true; do
    PICK="${ENTRIES[$((RANDOM %% ${#ENTRIES[@]}))]}"
    apply_wallpaper "$PICK"
    # Single-entry themes (e.g. a lone Lottie/video wallpaper) still need
    # one apply to start mpvpaper/hyprpaper; after that there's nothing to
    # cycle to, so exit cleanly.
    if [[ ${#ENTRIES[@]} -le 1 ]]; then
        exit 0
    fi
    sleep "$INTERVAL"
done
`, theme.Name, theme.Wallpapers.CycleInterval, theme.Wallpapers.CycleInterval, theme.Wallpapers.FitMode, imageEntries, videoEntries), nil
}

