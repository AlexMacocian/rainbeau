func wallpaperArrayEntries(paths []string, allowAbsolute bool) string {
	entries := make([]string, 0, len(paths))
	for _, path := range paths {
		if allowAbsolute && filepath.IsAbs(path) {
			entries = append(entries, fmt.Sprintf(`  "%s"`, path))
		} else {
			entries = append(entries, fmt.Sprintf(`  "$WALLPAPER_DIR/%s"`, path))
		}
	}
	return strings.Join(entries, "\n")
}

func buildImageHyprlockBackground(images []string, isLight bool) string {
	pick := images[rand.Intn(len(images))]
	brightness := "0.6"
	if isLight {
		brightness = "1.2"
	}
	return fmt.Sprintf(`background {
    path = ~/.config/hypr/wallpapers/%s
    blur_passes = 3
    blur_size = 6
    brightness = %s
}
`, pick, brightness)
}

func buildSolidHyprlockBackground(bg0 string) string {
	return fmt.Sprintf(`background {
    path =
    color = rgb(%s)
}
`, stripHash(bg0))
}

func stripHash(hex string) string {
	return strings.TrimPrefix(hex, "#")
}
