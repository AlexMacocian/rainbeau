type BluetoothScriptGenerator struct{}

func (BluetoothScriptGenerator) Name() string { return "Bluetooth Script" }
func (BluetoothScriptGenerator) OutputPath() string {
	return ".config/hypr/scripts/bluetooth-status.sh"
}

func (BluetoothScriptGenerator) Generate(theme *rainbeau.Theme, wallpapersDir string) (string, error) {
	c := theme.Colors
	sep := theme.Waybar.Separator
	return fmt.Sprintf(`#!/usr/bin/env bash
# Auto-generated by Rainbeau — do not edit manually
# Theme: %s
# Bluetooth status for waybar custom module

ICON=$'\uf294'
SEP="<span color='%s'>%s</span>"

powered=$(bluetoothctl show 2>/dev/null | grep -c "Powered: yes")
if [[ "$powered" -eq 0 ]]; then
    printf '{"text": "%%s off %%s", "tooltip": "Bluetooth off", "class": "off"}\n' "$ICON" "$SEP"
    exit 0
fi

connected=$(bluetoothctl devices Connected 2>/dev/null)
if [[ -n "$connected" ]]; then
    device_name=$(echo "$connected" | head -1 | cut -d' ' -f3-)
    count=$(echo "$connected" | wc -l)
    if [[ "$count" -gt 1 ]]; then
        printf '{"text": "%%s %%s %%s", "tooltip": "%%s devices connected", "class": "connected"}\n' "$ICON" "$count" "$SEP" "$count"
    else
        printf '{"text": "%%s %%s %%s", "tooltip": "Connected: %%s", "class": "connected"}\n' "$ICON" "$device_name" "$SEP" "$device_name"
    fi
else
    printf '{"text": "%%s on %%s", "tooltip": "Bluetooth on, no devices", "class": "on"}\n' "$ICON" "$SEP"
fi
`, theme.Name, c.Accent1, sep), nil
}

type GpuScriptGenerator struct{}

func (GpuScriptGenerator) Name() string       { return "GPU Script" }
func (GpuScriptGenerator) OutputPath() string { return ".config/hypr/scripts/gpu-usage.sh" }
func (GpuScriptGenerator) Generate(theme *rainbeau.Theme, wallpapersDir string) (string, error) {
	c := theme.Colors
	sep := theme.Waybar.Separator
	return fmt.Sprintf(`#!/usr/bin/env bash
# Auto-generated by Rainbeau — do not edit manually
# Theme: %s
# Auto-detected GPU usage for waybar

ICON=$'\uf26c'
SEP="<span color='%s'>%s</span>"

get_nvidia() {
    if command -v nvidia-smi &>/dev/null; then
        usage=$(nvidia-smi --query-gpu=utilization.gpu --format=csv,noheader,nounits 2>/dev/null | head -1 | tr -d ' ')
        if [[ -n "$usage" ]]; then
            printf '{"text": "%%s %%s%%%% %%s", "tooltip": "NVIDIA GPU: %%s%%%%", "class": "nvidia"}\n' "$ICON" "$usage" "$SEP" "$usage"
            return 0
        fi
    fi
    return 1
}

get_amd() {
    for gpu in /sys/class/drm/card*/device/gpu_busy_percent; do
        if [[ -r "$gpu" ]]; then
            usage=$(cat "$gpu" 2>/dev/null)
            if [[ -n "$usage" ]]; then
                printf '{"text": "%%s %%s%%%% %%s", "tooltip": "AMD GPU: %%s%%%%", "class": "amd"}\n' "$ICON" "$usage" "$SEP" "$usage"
                return 0
            fi
        fi
    done
    return 1
}

get_nvidia || get_amd || printf '{"text": "%%s N/A %%s", "tooltip": "No GPU detected", "class": "none"}\n' "$ICON" "$SEP"
`, theme.Name, c.Accent1, sep), nil
}

func f(value float64) string {
	return strings.TrimRight(strings.TrimRight(fmt.Sprintf("%.6f", value), "0"), ".")
}
