type HyprpaperGenerator struct{}

func (HyprpaperGenerator) Name() string       { return "Hyprpaper" }
func (HyprpaperGenerator) OutputPath() string { return ".config/hypr/hyprpaper.conf" }
func (HyprpaperGenerator) Generate(theme *rainbeau.Theme, wallpapersDir string) (string, error) {
	images := theme.Wallpapers.Images
	if len(images) == 0 {
		return fmt.Sprintf(`# Auto-generated by Rainbeau — do not edit manually
# Theme: %s
# (No static images; wallpaper handled by mpvpaper.)

splash = false
ipc = true
`, theme.Name), nil
	}
	pick := images[rand.Intn(len(images))]
	var preloads []string
	for _, image := range images {
		preloads = append(preloads, fmt.Sprintf("preload = ~/.config/hypr/wallpapers/%s", image))
	}
	return fmt.Sprintf(`# Auto-generated by Rainbeau — do not edit manually
# Theme: %s

%s

wallpaper {
    monitor =
    path = ~/.config/hypr/wallpapers/%s
}

splash = false
ipc = true
`, theme.Name, strings.Join(preloads, "\n"), pick), nil
}

